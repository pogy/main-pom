    /**
     * 按名字拿
     *
     * @return
     */
    public SpreadEnum parseToWOMAN() {
        if (this.code.startsWith("MAN-")) {
            String womanCode = this.code.replace("MAN", "WOMAN");
            for (SpreadEnum is : SpreadEnum.values()) {
                //查出不是自己
                if (is.getCode().equals(womanCode)) {
                    return is;
                }
            }
        }
        return null;
    }

    public static SpreadEnum getSpreadEnumByCode(String code) {
        if (code == null) {
            return null;
        }
        for (SpreadEnum spreadEnum : values()) {
            if (spreadEnum.code.equals(code)) {
                return spreadEnum;
            }
        }
        return null;
    }

